a=10
b=2.5
#operator aritmatika (+) penjumlahan
print(type(a+a))
print(type(a+b))
print(type(b+a))
print(type(b+b))

#operator aritmatika (-) pengurangan
print(type(a-a))
print(type(a-b))
print(type(b-a))
print(type(b-b))

#operator aritmatika (*) perkalian 
print(type(a*a))
print(type(a*b))
print(type(b*a))
print(type(b*b))

#operator aritmatika (/) pembagian
print(type(a/a))
print(type(a/b))
print(type(b/a))
print(type(b/b))

#operator aritmatika (//) pembagian dengan pembulatan ke bawah
print(type(a//a))
print(type(a//b))
print(type(b//a))
print(type(b//b))

#operator aritmatika (%)sisa hasil bagi 
print(type(a%a))
print(type(a%b))
print(type(b%a))
print(type(b%b))

#operator aritmatika (**) perpangkatan
print(type(a**a))
print(type(a**b))
print(type(b**a))
print(type(b**b))















